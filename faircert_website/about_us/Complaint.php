The complaint will be registered in the complaint register maintained for the purpose. After preliminary understanding of the nature of complaint Certification Manger appoints a investigator to have investigation on the complaint and submit report to CEO. The Complainant may be called to present the issue to CEO if needed. The CEO investigate the issue by studying all related records and documents for review and comes out with his decision. The decision will be communicated to the Complainant.
