<b>Appeals: </b><p></p>
In case of any decision taken by FairCert is not agreeable to client the client can make an appeal against that decision. <p></p>
The Certification Committee/CEO would first attend to all written appeals received. The complaint will be registered in the appeal register maintained for the purpose. The receipt of the appeal received will be given to appellant either by giving a signed copy of the appeal or by replying to the appeal mail. </br>
After preliminary understanding of the nature of Appeal Certification Committee/CEO decide whether the appeal to be accepted or rejected. If appeal is accepted an assessor is appointed to study the appeal properly and submit a report to appeal committee. </br>
The Appeal committee investigates the issue by studying all related records and documents for review and comes out with his decision. The decision will be communicated to the Appellant.<p></p> 

If the appellant is not happy with the decision appellant may be re-appeal to Appeal committee or CEO within 30 days of appeal decision.<p></p>

