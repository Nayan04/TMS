      <h6>Organic Certification Wild Harvest</h6>
 <div style="padding:20px; margin-top:-25px;">   
Millions of people live in and nearby forest area depends on the forest. They collect the forest produce for their livelihood without disturbing the forest.<p>
<p> The high consumer demand and steady growth in recent years demand of forest produce is gradually increasing in the world market due to use of forest products in food and medicinal industry. Therefore wild harvest certification offers opportunities for tribal communities or collectors whose livelihoods are depend to forest resources. </p>
<p>     
Wild harvest collection refers to the collection of medicinal and aromatic plants as well as sea and aquatic plant products (like seaweed) from their natural habitats. The main purpose of certifying the collection of wild plants/crops is to show that the collection does not endanger the ecosystem, and that the stability of the plant population or the ecosystem is not threatened.  </p> 
 <div class="mystyle" style="width:400px;">Benefits of Organic Certification of Wild Harvest</div><br />
 <ul>
<li>The collection should come from a clearly defined collection area. 
<li>The wild harvest certification helps to conserve endangered or threaten species available in the forest. 
<li>Protect the habitat losses and to a complete depletion of these resources. 
<li>Protect natural resources.
<li>Certification marks help to recognize your company logo and access to local, regional, and international markets.
<li>Organic certification helps in building trust between person involved in production, processing and trading of organic products.
<li>The traceability system for tracking of a product from its collection area to the place where it is consumed. 
</ul>

 <div class="mystyle">Definition of Organic</div><br />
<p>Organic is a particular farming system as described in standards and not to the term used in chemistry.
 <div class="mystyle">Organic Agriculture</div><br />
<p>Organic agriculture is a system of farm design and management to create an ecosystem, which can achieve sustainable productivity without the use of artificial external inputs such as chemical fertilizers and pesticides.
 <div class="mystyle">Organic Certification</div><br />
<p>Organic certification is a process by which the accredited Certification Body by way of a Scope Certificate assures that the production or processing system of the client has been methodically assessed and conforms to the specific standard requirements. 
 <div class="mystyle" style="width:400px;">Requirement for Organic Certification of Wild Harvest</div><br />
<p>A person seeking an organic certification of wild harvest must fulfill the following requirements </p>
<ul>
<li>The collection of wild plants and parts thereof, grown naturally, and not received any treatment with products other than those authorized for use in organic production. 
<li>Organic operators should collect products only from within the boundaries of the clearly defined wild collection area.
<li>Organic collection management should ensure that in case of minor forest produce collection, law of the land should be applicable and should not exceed the sustainable yield of the collected species or otherwise threaten the local ecosystem. 
<li>Products can only be certified organic if derived from a designated area for collection, clearly depicted in the map of the authorized area of collection by the forest department or state department, which is subject to inspection. 
<li>The collection area should be at an appropriate distance from conventional farming, pollution and contamination. 
<li>All records pertaining to production, storage, purchase and sales available and maintain all records at least five years beyond its creation.
</ul>  
