<div class="mystyle">Organic Certification </div><br />
<p>Organic Certification is a process by which the accredited Certification Body by way of a Scope Certificate assures that the production or processing system of the client has been methodically assessed and conforms to the specific standard requirements.</p>

<div class="mystyle">Processed Products </div><br />
<p>Processed product shall mean foodstuffs resulting from the processing of unprocessed products.
<div class="mystyle">Labelling</div><br />
<p>Labeling shall mean any written, printed or graphic representation that is depicted on the label of the certified organic product, for the purpose of promoting its sale.
<div class="mystyle">Food Additive</div><br />
<p>A food additive is any substance added as supplement or as enrichment, influencing the keeping quality and consistency of a food product.
<div class="mystyle">Processing Aids</div><br />
<p>A substance or material not consumed as a food ingredient by itself but used in the processing of raw materials, food or its ingredients to fulfill a certain technological purpose during treatment or processing.</p>
<div class="mystyle" style="width:500px;">Requirement for Organic Certification of Food Processing & Handling</div><br /><br />
The organic certification requirement of food processing & handling varies from country to country and involves a set of processing & handling standards for processors and handlers. A person seeking an organic certification of processing & handling must fulfill the following requirements 
<ul>

<li>The processing and handling of organic products should be optimized to maintain the quality and integrity of the products. 
<li>Establish, implement and update annually an Organic Plan â€“ Processing & Handling. 
<li>The pests should be avoided by good manufacturing practices and physical, mechanical, biological methods should be used to control pest. 
<li>The 100% organic ingredients should be used except where an organic ingredient is not available in sufficient quality or quantity; non organic ingredients may be used in case of technological need or for particular nutritional purposes. 
<li>The processing methods should be based on mechanical, physical, biological processes, smoking, extraction, precipitation and filtration. Extraction should be either with water, ethanol, plant and animal oils, vinegar, carbon dioxide, nitrogen or carboxylic acids. 
<li>Biodegradable, recyclable, reusable systems and eco-friendly packaging materials should be used. The packaging material should be food grade. 
<li>Single ingredient products should be labeled as 'Organic' and multi ingredient products where minimum 95% of the ingredients are certified organic origin, products may be labeled 'Certified Organic'. Where organic ingredient is less than 95% but not less than 70%, products may not be called 'organic'. The product is labeled as 'made with organic ingredients'.     
<li>Where a certified organic ingredient is less than 70%, the organic ingredients may appear in the ingredient list. 
<li>If herbs and/or spices constitute less than 2% of the total weight of the product, they may be listed as "spices" or "herbs" without stating the percentage. 
<li>The product integrity should be maintained during storage and transportation of organic products. No commingling or contamination of organic products during processing and protect organic products from contact with prohibited substances.
<li>No use of GMOs or irradiation is allowed.
<li>All records pertaining to production, storage, purchase and sales available and maintain all records at least five years beyond its creation.  
</ul>
