      <h6>Grower Group Certification</h6>
     <p>
     <div style="padding:20px; margin-top:-45px;">
       In India, most of the farmer’s community, having a small & marginal land holding. These farmers are not able to afford the cost of organic certification. But the organic agriculture movement will not be successful unit it reaches the small & marginal farmers. Previously, Organic Standards and Certification procedures were designed considering only individual farmers. Later on the potential for small & marginal group certification being realized and cooperatives or groups of producers located in a geographical or social region, whose crops are marketed collectively being certified.</p><p> In present scenario, Group Certification is most popular and acceptable amongst the Indian farming community.
In India, Agricultural & Processed Food Products Export Development Authority (APEDA), Ministry of Commerce & Industry, Government of India have developed Guidelines for Certification of Grower Group for small & marginal farmer’s group. The concept of grower group certification is recognized by USDA – NOP and develops the grower group certification policy. </p>

<div class="mystyle">Grower Groups </div><br />
<p>Grower Groups are organized group of producers who intend to produce organic products/engage in organic processes in accordance with the applicable standards.<br />
<div  class="mystyle">Internal Control System (ICS) </div><br />
<p>Internal Control System means the control system organized by the member farmers in the grower group to ensure that the standard requirements are met. <br />
<div class="mystyle">Service Provider </div><br />
<p>The service provider is an external body (e.g., Self-Help Groups / NGOs / Private Agency / State Govt. Agency) contracted by Grower Groups for maintaining the documentation, training, quality control, facilitating certification by an accredited Certification Body and for marketing of the produce of the Grower Groups.  <br />

  <div  class="mystyle" style="width:40%;"> Benefits of Grower Group Certification</div> <br />
  <ul>
  
<li>	Organic certification helps in building trust between person involved in production, processing and trading of organic products.
<li>	Certification marks help to recognize your group/company logo and access to local, regional, and international markets.
<li>	The cost of certification per hectare per year is very low in comparison to individual farmer certification.
<li>	The group will market the products under a single entity and able to bargaining with buyers. 
<li>	The group will linked with the national and international buyers. 
<li>	The Government Department will easily implement the departmental schemes. 
</li></ul>
 <div class="mystyle" style="width:40%;">Requirement for Grower Group Certification</div><br />
<p>A grower group, SHGs / NGOs / Private Agency / State Govt. Departments seeking a Grower Group Certification must fulfill the following requirements
<ul>
<li>	The grower group certification is based on Internal Control System (ICS) and applies groups, farmers, cooperatives, contract production and small scale processing units. The producer in the group must apply similar production systems and the farms should be in geographical proximity.  
<li>	The group consists of minimum 25 and maximum 500 farmers. Individual farmer with land holding of 4 hectares and above part of the group but will have to be inspected separately every year by the FairCert. 
Note: The farm or farmer having total area is 3.90 hectares must be inspected annually as per the latest circular received from APEDA. 
<li>	The ICS have a registered legal identity and have a constitution of the organization. The responsibilities of ICS staff must be defined according to the organizational chart. 
<li>	In case the farmers cannot run the ICS, they may enter into a contract with an external service provider.
<li>	The grower group must have a written contract with each member confirming the member understanding and compliance with the relevant standards, permitting internal inspections and external inspections. The contract may be written in the local language.   
<li>	Grower Group Certification is based on the concept of an Internal Quality Management System (IQMS). The ICS manual must include policy and procedures related to implementation of the ICS, internal standards and risk assessment. 
<li>	The ICS must have sufficient and competent personnel, financial and technical resources to manage and implement a system incorporating the Management Plan and to maintain conformity with the relevant Standard.
<li>	The internal standards must be prepared in local language by the ICS Manager. The internal standards would contain the definition of the production unit, deal with part conversion, conversion period, Maintenance of buffer zone, Farm production norms and Harvest & post harvest procedures.
<li>	The ICS personnel should not have any conflict of interest that might hinder the work. All possible conflicts should be declared in a written statement.  
<li>	The certification should be granted according to the applicable standard. 
<li>	The products will be marketed under a single entity and develop the relevant procedure. 
<li>	The ICS should develop the procedure for registration of members, provision of documents supplied by ICS to the group member and provision of exit of the member from grower group.  
<li>	The ICS should prepare the operating documents such as overview map, farmer list, sanction farmers list and risk assessment. The operating documents must be available at ICS office. 
<li>	The risk assessment of the grower group should be conduct every year. 
<li>	The internal inspection must be conduct at least twice in a year by the internal inspector. 
<li>	At least one internal inspector per 50 – 60 farmers for ensuring 100% internal inspection of all farmers in the group is carried out at least twice a year (one in growing season of each crop). 
<li>	The inspection will be carried out in presence of the member or his representative and inspection checklist duly signed by inspector and the member or his representative.  
<li>	The ICS will have a defined procedure to approve or impose sanction on the farmers in the group. All internal farm checklists are screened by internal approval committee or approval manager with special focus on the critical control points of risk / difficult cases. 
<li>	The accredited certification body will inspect some of the farms for the evaluation of the grower group for efficient ICS on the basis of risk perceived by the inspector. 
<li>	Yield will be estimated for each crop for individual farmer in the group by the ICS. 
<li>	The ICS should develop the procedures for deal with non compliance and sanctions. The ICS should maintain a sanction record.       
<li>	A competent person will train each internal inspector annually and date of training, participants and topic should be documented. 
<li>	Regular farmer training will be organized by ICS and ensure that each farmer receive at least one initial advisory visit by the extension service or in an organized training. The farmer training should be documented. 
<li>	The ICS should develop the buying procedure and ensure that integrity of the products must be maintained. The buying records should be clearly indicating the status of the products and bags should be labeled as ‘Organic’ or as ‘in-conversion’.
<li>	The ICS should develop the storage and handling procedure and ensure that integrity of the products must be maintained. The products should be identifying at all stages of product flow during transition and labeled. Fumigation of containers, irradiation and ionization are prohibited. 
<li>	The certification body will inspect central processing unit. The processing steps will be documented and the products should be separated from non organic products during processing.     
</ul>

